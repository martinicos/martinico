// Crea un botón para contactar por correo electrónico
const btn = document.createElement('button');
btn.innerHTML = 'Contáctame!';

// Agrega un evento para abrir el correo electrónico al hacer clic en el botón
btn.addEventListener('click', function () {
  // Usa la propiedad location.href del objeto window para redirigir a la dirección de correo
  window.location.href = 'mailto:johndoe@example.com';
});

// Agrega el botón al documento HTML
document.body.appendChild(btn);
